/**
 * Global is an interface that is implemented by any class to maintain shared constants such as empty block, game draw,
 * playerX and playerO as integers for tracking.
 */

public interface Global {

    public static final int DRAW = 3;
    public static final int EMPTY = 0;
    public static final int PLAYERX = 1;
    public static final int PLAYERO = 2;

}
